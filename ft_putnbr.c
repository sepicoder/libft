#include "libft.h"


void	ft_putnbr(int n)
{
	int i;
	int sign;

	sign = -1;
	i = 0;
	if (n == -2147483648)
		ft_putstr("-2147483648");
	if (n >= 0)
		i = n;
	if (n < 0)
	{
		i = n * sign;
		ft_putchar('-');
	}
	if (i > 10)
		ft_putnbr(i / 10);
	ft_putchar((i % 10) + '0');
}

